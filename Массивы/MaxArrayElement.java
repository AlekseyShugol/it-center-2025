public class MaxArrayElement {
    public static void main1(String[] args) {
        /// Задача 2: Найти максимальный элемент в массиве
        
        // Создаем массив с произвольными числами
        int[] numbers = {2, 5, 8, 3, 7, 1, 9, 4, 6};
        
        // Предполагаем, что первый элемент - максимальный
        // Это наша начальная точка для сравнения
        int max = numbers[0];
        
        // Начинаем цикл со второго элемента (индекс 1), 
        // так как первый элемент уже взят за начальное значение
        for(int i = 1; i < numbers.length; i++) {
            // Сравниваем текущий элемент с текущим максимумом
            // Если текущий элемент больше, обновляем значение максимума
            if(numbers[i] > max) {
                max = numbers[i];
            }
        }
        
        // Выводим найденный максимальный элемент
        System.out.println("Максимальный элемент: " + max);
    }
}