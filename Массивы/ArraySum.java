public class ArraySum {
    public static void main1(String[] args) {
        /// Задача 1: Найти сумму всех элементов массива

        // Создаем массив с произвольными числами
        int[] numbers = {2, 5, 8, 3, 7, 1, 9, 4, 6};

        // Инициализируем переменную для хранения суммы, начинаем с 0
        int sum = 0;

        // Проходим по всем элементам массива с помощью цикла for
        // i - это индекс текущего элемента, начинаем с 0 (первый элемент)
        // Условие i < numbers.length - продолжаем пока не дойдем до конца массива
        // i++ - увеличиваем индекс на 1 после каждой итерации
        for(int i = 0; i < numbers.length; i++) {
            // На каждой итерации добавляем текущий элемент к сумме
            // numbers[i] - получаем элемент массива по индексу i
            sum += numbers[i];
        }

        // Выводим результат после завершения цикла
        System.out.println("Сумма элементов: " + sum);
    }
}